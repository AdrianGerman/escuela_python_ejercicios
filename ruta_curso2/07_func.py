# Funcion para multiplicar por 2 todo el resultado obtenido
def my_print(text):
    print(text * 2)

# Funcion que hace la suma de 2 numeros proporcionados
def suma(a, b):
    print(a + b)
    
# usando la primer función
my_print('Este es mi texto')
my_print('Hola, mundo')

# utilizando la segunda función
suma(10, 5)
suma(3143, 24)
    